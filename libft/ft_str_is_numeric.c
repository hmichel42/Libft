#include "includes/libft.h"

int		ft_str_is_numeric(char *str)
{
	if (str == NULL)
		return (0);
	while (*str <= 57 && *str >= 48 && *str != '\0')
		str++;
	if (*str == '\0')
		return (1);
	return (0);
}
