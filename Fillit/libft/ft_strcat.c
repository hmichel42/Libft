#include "includes/libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	size_t	x;
	size_t	y;

	y = 0;
	x = ft_strlen(dest);
	while (src[y])
	{
		dest[x + y] = src[y];
		y++;
	}
	dest[x + y] = '\0';
	return (dest);
}
